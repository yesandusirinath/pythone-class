print("Square and cube calculator")
a = input("what do you want to calculate: ")
if a == "square":
    print("Calculating square")
    b = int(input("Enter the number: "))
    c = b**2
    print("The square of the number is: ",c)
elif a == "cube":
    print("calculating cube")
    e = int(input("Enter the number: "))
    f = e**3
    print("The cube of the number is: ",f)

    
